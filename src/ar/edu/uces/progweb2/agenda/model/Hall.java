package ar.edu.uces.progweb2.agenda.model;

public class Hall {
	
	private Long id;
	private String description;
	
	public Hall(){
		
	}
	
	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
	
}
